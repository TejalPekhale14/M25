package com.tns.operator;

public class ArithmeticOperator {

	public static void main(String[] args) {
		int a=10,b=10,c;
		c=a+b;
		System.out.println(c);
		c=a-b;
		System.out.println(c);
		c=a*b;
		System.out.println(c);
		c=a/b;
		System.out.println(c);
		c=a%b;
		System.out.println(c);
	}

}
